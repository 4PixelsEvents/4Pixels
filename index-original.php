              <h2>Perzië komt bij Civilization VI</h2>
              <p>Perzië voegt zich bij Polen en Australië als de nieuwste DLC voor Civilization VI.</p>
              <p>Vorige maand voegde Australië zich bij de Civilization rangen voor het allereerst als Premium DLC. Vorig jaar werd Polen al toegevoegd. Nu heeft Firaxis aangekondigd dat Perzië de volgende DLC beschaving die in het spel komt in een lente update die ook nog Macedonië toevoegt, geleid door Alexander de Grote</p>
              <p>Aan het roer staat Cyrus II “The Great”, die ervoor zorgde dat het oude Perzië groeide van de Middellandse Zee en de Dardanellen in het huidige Turkije tot aan de Indus rivier in het oosten. Cyrus II creëerde hiermee het grootste rijk wat de wereld ooit gezien had. Hij regeerde ongeveer 29 tot 31 jaar, en stierf terwijl hij oorlog voerde tegen de Massageten. Toen Alexander de Grote Persepolis binnenviel bezocht hij speciaal de Tombe van Cyrus, die nog steeds bestaat en op de UNESCO World Heritage lijst staat.</p>
