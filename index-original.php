              <p>Nintendo zegt dat de problemen met de verbindingen komen door een “productie variatie”</p>
              <p>Sinds zelfs nog voor de release zijn er klachten gekomen over verbindingsproblemen met de Nintendo Switch’s linker Joy-Con controller. Nu is er gesuggereerd dat deze problemen komen door het ontwerp van de controller, omdat de rechter Joy-Con controller een eigen antenne component heeft, en de linker niet. De Bluetooth antenne in de linker Joy-Con controller is namelijk niet een los onderdeel, maar is vast gesoldeerd op de printplaat en die zit eer gelijk naast de behuizing voor de joystick. Dit is een simpel metalen doosje, net onder de shoulder button, en het gebeurt vaak dat deze plek bedekt word door je vingers als je aan het gamen bent. YouTuber Spawn Wave heeft de problemen kunnen oplossen door een koperdraadje te solderen aan het printplaatje om de antenne naar beneden te verplaatsen.</p>
