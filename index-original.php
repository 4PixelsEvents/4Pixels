              <h2>Blizzard ‘staat open’ voor een map editor in Overwatch.</h2>
              <p>Misschien kan je zelf maps gaan maken voor Overwatch, maar het zal niet binnenkort zijn. </p>
              <p>Wij bij 4 Pixels zijn fan van Overwatch, en we weten dat er een hoop maps beschikbaar zijn momenteel, maar wat als je je eigen map kon maken? Misschien is het ooit een keer mogelijk zegt Game Director Jeff Kaplan.</p>
              <p>In een post op de officiële forums zegt Kaplan namelijk dat het team dit wel wou maken, maar dat de volledig nieuwe engine waar Overwatch op gebouwd is dit erg moeilijk zou maken en dat dit veel tijd zou kosten.</p>
              <p>"We are extremely open-minded about releasing a map editor for Overwatch someday. But because Overwatch was made with a brand new engine, this is not a small task or one which can happen any time soon. We have this on our long-term road map and believe heavily in user-made content. But there are many challenges ahead of us and it will be a very long road before our editor can be made available to the public."</p>
